package Package1;

public class EmpleadoContrato extends Empleado1 {
        
        // definimos los atributos 
        private int valorContrato;

        // definimos el constructor de EmpleadoEfectivo con sus atributos + los heredados 
        // super llama al constructor Padre 
        public EmpleadoContrato(int dni , String nombre , String apellido, int valorContrato) {
              super(dni, nombre, apellido);
              this.valorContrato = valorContrato;
        }

        @Override
        public int calcularSueldo(){
            return this.valorContrato;
        }

} 




























































































































































































